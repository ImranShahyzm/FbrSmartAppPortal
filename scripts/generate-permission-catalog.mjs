import fs from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const manifestPath = path.join(repoRoot, 'shared', 'permission-catalog.manifest.json');
const outPath = path.join(
  repoRoot,
  'backend',
  'FbrSmartApp.Api',
  'Services',
  'PermissionCatalog.Generated.cs'
);

function esc(s) {
  return String(s).replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

function validateManifest(manifest) {
  if (!manifest || typeof manifest !== 'object') throw new Error('manifest must be an object');
  if (!Array.isArray(manifest.apps)) throw new Error('manifest.apps must be an array');
  for (const [i, app] of manifest.apps.entries()) {
    if (!app || typeof app !== 'object') throw new Error(`apps[${i}] must be an object`);
    for (const k of ['appId', 'permissionsPrefix', 'displayName']) {
      if (typeof app[k] !== 'string' || !app[k].trim()) {
        throw new Error(`apps[${i}].${k} must be a non-empty string`);
      }
    }
    if (!Array.isArray(app.resources)) throw new Error(`apps[${i}].resources must be an array`);
    for (const [j, r] of app.resources.entries()) {
      if (!r || typeof r !== 'object') throw new Error(`apps[${i}].resources[${j}] must be an object`);
      if (typeof r.key !== 'string' || !r.key.trim())
        throw new Error(`apps[${i}].resources[${j}].key must be a non-empty string`);
      if (typeof r.label !== 'string' || !r.label.trim())
        throw new Error(`apps[${i}].resources[${j}].label must be a non-empty string`);
    }
  }
}

const raw = await fs.readFile(manifestPath, 'utf8');
const manifest = JSON.parse(raw);
validateManifest(manifest);

const appsCs = manifest.apps
  .map(app => {
    const resourcesCs = app.resources
      .map(r => `                new PermissionCatalog.ResourceEntry("${esc(r.key)}", "${esc(r.label)}"),`)
      .join('\n');
    return `        new PermissionCatalog.AppCatalogEntry(
            "${esc(app.appId)}",
            "${esc(app.permissionsPrefix)}",
            "${esc(app.displayName)}",
            [
${resourcesCs}
            ]),`;
  })
  .join('\n');

const cs = `// <auto-generated/>
// Generated by scripts/generate-permission-catalog.mjs from shared/permission-catalog.manifest.json
namespace FbrSmartApp.Api.Services;

internal static class PermissionCatalogGenerated
{
    internal static readonly IReadOnlyList<PermissionCatalog.AppCatalogEntry> Apps =
    [
${appsCs}
    ];
}
`;

await fs.mkdir(path.dirname(outPath), { recursive: true });
await fs.writeFile(outPath, cs, 'utf8');

process.stdout.write(`Generated PermissionCatalog: ${path.relative(repoRoot, outPath)}\n`);

